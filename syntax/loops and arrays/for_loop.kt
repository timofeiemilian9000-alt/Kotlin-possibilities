fun main() {
    
    val liquores = arrayOf( "Wisky", "Vodka", "Cognac", "Vermut", "Amaro del Capo" )

    for ( l in liquores ) {
        println( l ) // prints all the liquores
    }

    val numbers = arrayOf( 1, 10, 100, 1000, 10000 )

    for ( n in numbers ) {
        println( n ) // prints all the numbers
    }

    /*
        
        Unlike Java and other programming languages, 
        there is no traditional for loop in Kotlin.

        In Kotlin, the for loop is used to loop through 
        arrays, ranges, and other things that contains 
        a countable number of values.
        
     */
}