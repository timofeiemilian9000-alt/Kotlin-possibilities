fun main() {
    var name = "Emilian" // var can be changed
    val age = "29" // val can't be change

    println(name)
    println(age)

    // You can also use the + character to add a variable to another variable:
    val your_name = "Dan"
    println("Hi " + your_name)
}
/* 
 Kotlin is smart enough to understand that 
 "John" is a String (text), and that 1975 is an Int (number) variable.
*/
